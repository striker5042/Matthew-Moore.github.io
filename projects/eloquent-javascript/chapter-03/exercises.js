////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(x, y) {
if (x < y){
  return x;
} else {
  return y;
}

}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(x) {

if (x % 2 === 0) {
  return true;
} else {
  return false;
}

}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, char) {
var count = 0
for (var i = 0; i < str.length; i++) {
  if (str[i] === char) {
    count++
  }
}
return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str) {

var count = 0
for (var i = 0; i < str.length; i++) {
  if (str[i] === 'B') {
    count++
  }
}
return count;

}

